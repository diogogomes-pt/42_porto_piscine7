#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;
	
	i = '0';
	while(str[i] != '\n')
	{
		i++;
	}
	write (1, &i, 1);
	return (0);
}

int	main(void)
{
	ft_strlen("porto");
}
